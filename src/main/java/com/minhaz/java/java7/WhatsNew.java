package com.minhaz.java.java7;

public class WhatsNew {
    static char aChar;

    int A$B = 10;
    int _helloWorld = 10;
    int $helloWorld = 10;

    public static void main(String[] args) {
        // You can have underscores in numbers to make them easier to read from Java7 onwards
        int value = 100_000;
        System.out.println("value = " + value);
        System.out.println("char = " + aChar);
    }
}
